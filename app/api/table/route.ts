export async function GET(params: NextRequest) {
  const parameters = new URL(params?.url)?.searchParams;
  const searchText = parameters?.get("searchText");
  const page = parseInt(parameters?.get("page") ?? "1", 10);
  const pageSize = parseInt(parameters?.get("pageSize") ?? "10", 10);

  const startIndex = (page - 1) * pageSize;
  const endIndex = startIndex + pageSize;

  const filteredData = data?.length
    ? data?.filter((item) =>
        item?.player?.toLowerCase()?.includes(searchText!?.toLowerCase())
      )
    : data;
  const totalCount = filteredData?.length;
  const rows = filteredData?.slice(startIndex, endIndex);
  const result = {
    rows,
    totalCount,
  };

  return new Response(JSON.stringify(result));
}
